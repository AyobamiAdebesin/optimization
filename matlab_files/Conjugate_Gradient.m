function Conjugate_Gradient
Q = [3 0 1;0 4 2;1 2 3];
b = [3;0;1];
c = 0;
x0 = 1e3*eye(3,1);
g0 = Q*x0-b;
d0 = -g0;

disp('GRADIENT DESCENT ALGORITHM')
disp('__________________________________________________________________________')
disp('k ............ x1 .............x2 .............x3.............. f(xk) ....')
disp('__________________________________________________________________________')


for k=1:3
    alpha = -g0'*d0/(d0'*Q*d0);
    x0 = x0 + alpha*d0;
    f = x0'*Q*x0/2 - b'*x0 + c;
    fprintf('%2.0f ... %12.8e ... %12.8e ...%12.8e ... %12.8e\n', k, x0(1),x0(2),x0(3),f);
    g0 = Q*x0-b;
    beta0 = g0'*Q*d0/(d0'*Q*d0);
    d0 = -g0 + beta0*d0;
end
disp('EXACT SOLUTION')
disp('_________________________________________________________________________')
disp('x1 ...................x2 .................x3................ f(xk) ....')
disp('_________________________________________________________________________')
x = Q\b;
fprintf('%12.8e ... %12.8e ... %12.8e ...%12.8e\n',x(1), x(2), x(3),x'*Q*x/2 - b'*x + c);
end

